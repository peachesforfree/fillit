#include <string.h>
#include <unistd.h>

char	*ft_strrev(char *str)
{
	int		i;
	int		j;
	char	k;

	i = 0;
	if (str[i])
		i++;
	j = -1;
	while (++j < --i)
	{
		k = str[j];
		str[j] = str[i];
		str[i] = k;
	}
	return (str);
}
